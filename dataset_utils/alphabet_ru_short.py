alphabet = '/абвгдеёжзийклмнопрстуфхцчшщъыьэюя0123456789,;!:.-'

def enum_alphabet():
    dic = {}
    for ind, ch in enumerate(alphabet):
        dic[ind] = ch
    return dic

def inv_enum_alphabet():
    dic = {}
    for ind, ch in enumerate(alphabet):
        dic[ch] = ind
    return dic

def colored_alphabet():
    import random
    dic = {}
    for ch in alphabet:
        dic[ch] = (random.randint(0, 255),
                   random.randint(0, 255),
                   random.randint(0, 255))
    return dic


